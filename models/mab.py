class ISABMultiLabel(nn.Module):
    def __init__(self, dim_in, dim_out, num_heads, num_classes, ln=False):
        super(ISABMultiLabel, self).__init__()
        self.I = nn.Parameter(torch.Tensor(1, num_classes, dim_out))
        nn.init.xavier_uniform_(self.I)
        self.mab = MABMultiLabel(dim_out, dim_in, dim_out, num_heads, ln=ln)
        self.sab1 = SAB(dim_in, dim_in, num_heads, ln=ln)
        self.sab2 = SAB(dim_in, dim_in, num_heads, ln=ln)
        self.sab3 = SAB(dim_out, dim_out, num_heads, ln=ln)

    def forward(self, X):
        X = self.sab1(X)
        X = self.sab2(X)
        H, A = self.mab(self.I.repeat(X.size(0), 1, 1), X) # B, L, C; B, L, HW
        H = self.sab3(H)
        return H, A